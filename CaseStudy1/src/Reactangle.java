
public class Reactangle {
	private int length;
	private int breadth;
	private int area;
	public Reactangle(int length, int breadth, int area) {
		super();
		this.length = length;
		this.breadth = breadth;
		this.area = area;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getBreadth() {
		return breadth;
	}
	public void setBreadth(int breadth) {
		this.breadth = breadth;
	}
	public int getArea() {
		return area;
	}
	public void setArea(int area) {
		this.area = area;
	}
	
	

}
