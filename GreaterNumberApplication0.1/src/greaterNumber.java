
public class greaterNumber {
	public int greaterNumber(int num1,int num2)
	{
		if(num1>num2)
		{
			return num1;
		}
		else
		{
			return num2;
		}
		
	}
	
	public float greaterNumber(float num1,float num2)
	{
		
		if(num1>num2)
		{
			return num1;
		}
		else
		{
			return num2;
		}
	}
	public char greaterNumber(char a,char b)
	{
		if(a>b)
		{
			return a;
		}
		else
			return b;
	}
}
