
public class Book {
	private String tital;
	private int price;
	public Book(String tital, int price) {
		
		this.tital = tital;
		this.price = price;
	}
	public String getTital() {
		return tital;
	}
	public void setTital(String tital) {
		this.tital = tital;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
